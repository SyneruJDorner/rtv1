#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dst;
	size_t	i;

	i = 0;
	while (src[i] != '\0')
		i++;
	dst = ft_memalloc(sizeof(char) * i + 1);
	if (!dst)
		return (0);
	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
