#include "../../../constructs.h"

void	vector3_add_vector3(t_vector3 v1, t_vector3 v2, t_vector3 *ret)
{
	ret->x = v1.x + v2.x;
	ret->y = v1.y + v2.y;
	ret->z = v1.z + v2.z;
}
